    - é um conceito fundamental da programação orientada a objetos que permite criar uma nova classe 
    (chamada de classe filha ou subclasse) com base em uma classe existente (chamada de classe pai ou superclasse). 
    - A classe filha herda os atributos e métodos da classe pai, o que promove a reutilização de código e a criação de hierarquias de classes. 
    - A herança é representada pelo operador "extends" em PHP. 

    Encapsulamento:
    - é um princípio da programação orientada a objetos que consiste em ocultar os detalhes internos de uma classe
    e expor apenas o necessário para o uso externo. 
    - Isso é feito através do uso de modificadores de acesso (public, private, protected) para controlar 
    a visibilidade dos atributos e métodos de uma classe. 
    - O encapsulamento ajuda a proteger os dados e a garantir que a classe seja usada de maneira adequada, 
    evitando que o código externo interfira diretamente nos detalhes internos da classe.

    Polimorfismo:
    - é um conceito da programação orientada a objetos que permite que objetos de diferentes classes sejam tratados de maneira 
    uniforme, desde que compartilhem uma interface comum.
    - O polimorfismo é alcançado através de métodos que podem ser redefinidos (overridden) em classes filhas, 
    permitindo que cada classe forneça sua própria implementação do método, mesmo que o nome do método seja o mesmo em todas as classes.

    Abstração:
    - é um princípio da programação orientada a objetos que consiste em focar nos aspectos essenciais de um objeto,
    ignorando os detalhes desnecessários.
    - A abstração é alcançada através do uso de classes abstratas e interfaces, que definem um contrato para as classes que as implementam,
    sem fornecer uma implementação concreta.
